def place(grid_size: int, cells: np.ndarray = ORBIUM_CELLS, top_left=None) -> np.ndarray:
    """Return a `grid_size` square field with `cells` embedded once.

    `top_left` is the (row, col) of the seed's upper-left corner; it defaults
    to roughly centring the seed. The seed must fit inside the grid.
    """
    field = np.zeros((grid_size, grid_size), dtype=np.float64)
    h, w = cells.shape
    if top_left is None:
        top_left = ((grid_size - h) // 2, (grid_size - w) // 2)
    r0, c0 = top_left
    if r0 < 0 or c0 < 0 or r0 + h > grid_size or c0 + w > grid_size:
        raise ValueError(
            f"seed {cells.shape} at {top_left} does not fit in grid {grid_size}"
        )
    field[r0 : r0 + h, c0 : c0 + w] = cells
    return field
